package tcxt;

public class Zhdl {
	private String zh;
	private String mm;
	public String getZh() {
		return zh;
	}
	public void setZh(String zh) {
		this.zh = zh;
	}
	public String getMm() {
		return mm;
	}
	public void setMm(String mm) {
		this.mm = mm;
	}
	public Zhdl(String zh, String mm) {
		super();
		this.zh = zh;
		this.mm = mm;
	}
	public Zhdl() {
		super();
	}
}
