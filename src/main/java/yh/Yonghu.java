package yh;

public class Yonghu {
	private int id;
	private String xm;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getXm() {
		return xm;
	}
	public void setXm(String xm) {
		this.xm = xm;
	}
	public Yonghu(int id, String xm) {
		super();
		this.id = id;
		this.xm = xm;
	}
	public Yonghu() {
		super();
	}
	
}
